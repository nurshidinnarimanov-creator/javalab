public class ArithmeticOperations {
    public static void main(String[] args) {
        double x = 12;
        double y = 4;

        double sum = x + y;
        double multiply = x * y;
        double divide = x / y;

        System.out.println("Сложение: " + sum);
        System.out.println("Умножение: " + multiply);
        System.out.println("Деление: " + divide);
    }
}